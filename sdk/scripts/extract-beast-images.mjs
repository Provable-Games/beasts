/**
 * Extract data URIs from a Cairo file.
 * Each function contains a single .append(@"data:image/...;base64,...") call.
 *
 * @param {string} filePath - Path to the Cairo file
 * @param {RegExp} fnPattern - Regex to extract beast name from function name
 * @returns {Record<number, string>} beastId -> data URI
 */
function extractDataUris(filePath, fnPattern) {
  const content = readFileSync(filePath, "utf-8");
  const result = {};

  // Match function definitions and their data URIs
  // Pattern: fn get_<name>_..._svg() -> ByteArray { ... @"data:..." ... }
  const fnRegex = new RegExp(
    `fn (get_\\w+)\\(\\) -> ByteArray \\{[^}]*@"(data:image\\/[^"]+)"`,
    "gs"
  );

  let match;
  while ((match = fnRegex.exec(content)) !== null) {
    const fnName = match[1];
    const dataUri = match[2];

    // Extract beast name from function name using the provided pattern
    const nameMatch = fnName.match(fnPattern);
    if (!nameMatch) {
      console.warn(`Could not parse beast name from: ${fnName}`);
      continue;
    }

    const beastName = nameMatch[1].toLowerCase();
    const beastId = BEAST_NAME_TO_ID[beastName];
    if (!beastId) {
      console.warn(`Unknown beast name: ${beastName} (from ${fnName})`);
      continue;
    }

    result[beastId] = dataUri;
  }

  return result;
}

// Extract from all 4 files
const pngRegular = extractDataUris(
  resolve(BEASTS_SRC, "beast_png_regular_data.cairo"),
  /^get_(\w+)_svg$/
);
const pngShiny = extractDataUris(
  resolve(BEASTS_SRC, "beast_png_shiny_data.cairo"),
  /^get_(\w+)_shiny_svg$/
);
const gifRegular = extractDataUris(
  resolve(BEASTS_SRC, "beast_gif_regular_data.cairo"),
  /^get_(\w+)_regular_gif_svg$/
);
const gifShiny = extractDataUris(
  resolve(BEASTS_SRC, "beast_gif_shiny_data.cairo"),
  /^get_(\w+)_shiny_gif_svg$/
);

// Verify counts
for (const [label, map] of [
  ["PNG regular", pngRegular],
  ["PNG shiny", pngShiny],
  ["GIF regular", gifRegular],
  ["GIF shiny", gifShiny],
]) {
  const count = Object.keys(map).length;
  if (count !== 75) {
    console.error(`${label}: expected 75 beasts, got ${count}`);
    process.exit(1);
  }
  console.log(`${label}: ${count} beasts extracted`);
}

// Generate TypeScript output
function generateMapLiteral(map) {
  const entries = Object.entries(map)
    .sort(([a], [b]) => Number(a) - Number(b))
    .map(([id, uri]) => `  ${id}: "${uri}",`)
    .join("\n");
  return `{\n${entries}\n}`;
}

const ts = `/**
 * Beast image data URIs extracted from on-chain Cairo contracts.
 *
 * AUTO-GENERATED by scripts/extract-beast-images.mjs — DO NOT EDIT
 *
 * Contains inline base64 data URIs for all 75 beasts in 4 variants:
 * - PNG regular, PNG shiny, GIF regular, GIF shiny
 */

/** Beast ID (1-75) -> data:image/png;base64,... */
export const BEAST_PNG_REGULAR: Record<number, string> = ${generateMapLiteral(pngRegular)};

/** Beast ID (1-75) -> data:image/png;base64,... (shiny variant) */
export const BEAST_PNG_SHINY: Record<number, string> = ${generateMapLiteral(pngShiny)};

/** Beast ID (1-75) -> data:image/gif;base64,... */
export const BEAST_GIF_REGULAR: Record<number, string> = ${generateMapLiteral(gifRegular)};

/** Beast ID (1-75) -> data:image/gif;base64,... (shiny variant) */
export const BEAST_GIF_SHINY: Record<number, string> = ${generateMapLiteral(gifShiny)};

/**
 * Get the on-chain data URI for a beast image.
 *
 * @param beastId - Beast type ID (1-75)
 * @param shiny - Whether to use the shiny variant
 * @param animated - Whether to use the animated GIF variant (default: false for PNG)
 * @returns data:image/... base64 data URI, or empty string if not found
 */
export function getBeastImageDataUri(
  beastId: number,
  shiny: boolean,
  animated: boolean = false,
): string {
  if (animated) {
    return (shiny ? BEAST_GIF_SHINY[beastId] : BEAST_GIF_REGULAR[beastId]) ?? "";
  }
  return (shiny ? BEAST_PNG_SHINY[beastId] : BEAST_PNG_REGULAR[beastId]) ?? "";
}
`;

writeFileSync(OUTPUT, ts);
console.log(`\nWrote ${OUTPUT}`);
